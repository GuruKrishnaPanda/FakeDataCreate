package pojoClases;

public class protectionInstrument {
	
	public String instrumentId;
	private String protectionId;
	private String protectionAllocatedValue;
	private String chargeType;
	public String reportingEntityId;
	public String contractId;
	public String getInstrumentId() {
		return instrumentId;
	}
	public void setInstrumentId(String instrumentId) {
		this.instrumentId = instrumentId;
	}
	public String getProtectionId() {
		return protectionId;
	}
	public void setProtectionId(String protectionId) {
		this.protectionId = protectionId;
	}
	public String getProtectionAllocatedValue() {
		return protectionAllocatedValue;
	}
	public void setProtectionAllocatedValue(String protectionAllocatedValue) {
		this.protectionAllocatedValue = protectionAllocatedValue;
	}
	public String getChargeType() {
		return chargeType;
	}
	public void setChargeType(String chargeType) {
		this.chargeType = chargeType;
	}
	public String getReportingEntityId() {
		return reportingEntityId;
	}
	public void setReportingEntityId(String reportingEntityId) {
		this.reportingEntityId = reportingEntityId;
	}
	public String getContractId() {
		return contractId;
	}
	public void setContractId(String contractId) {
		this.contractId = contractId;
	}
	


}
